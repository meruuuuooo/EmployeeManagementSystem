    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        JTextField[] textFields = {this.IdTextField, this.fullnameTextField, this.departmentField, this.jobtitleField, this.salaryTextField};
        JComboBox[] comboBoxes = {this.monthComboBox, this.dayComboBox, this.yearComboBox};
        try {
            if (!isEmpty(textFields)) {
                String[] data = new String[6];
                boolean isDuplicate = false;
                int id = Integer.parseInt(textFields[0].getText());
                for (Object object : registeredData) {
                    String[] newData = (String[]) object;
                    int existingID = Integer.parseInt(newData[0]);
                    if (id == existingID) {
                        isDuplicate = true;
                        break;
                    }
                }
                if (isDuplicate) {
                    JOptionPane.showMessageDialog(this, "Duplicate ID! Please enter a different ID.", "Invalid", JOptionPane.ERROR_MESSAGE);
                } else {
                    for (int i = 0; i < textFields.length; i++) {
                        data[i] = textFields[i].getText();
                    }
                    data[5] = dateOfBirth(comboBoxes);
                    registeredData.add(data);
                    readBtnActionPerformed(evt);
                    JOptionPane.showMessageDialog(this, "Record Created Successfully", "Create Record", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Text Fields are Empty!", "Inavalid", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e, "Exception Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_createBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        JTextField[] textFields = {this.IdTextField, this.fullnameTextField, this.departmentField, this.jobtitleField, this.salaryTextField};
        JComboBox[] comboBoxes = {this.monthComboBox, this.dayComboBox, this.yearComboBox};
        DefaultTableModel table = (DefaultTableModel) this.employeeTable.getModel();
        int selectedRow = this.employeeTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "No Selected Row to Update!", "Error", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if (!isEmpty(textFields)) {
                String[] data = new String[6]; // Assuming you have 6 columns
                for (int i = 0; i < textFields.length; i++) {
                    String value = textFields[i].getText();
                    table.setValueAt(value, selectedRow, i);
                    data[i] = value;
                }
                String dob = dateOfBirth(comboBoxes);
                table.setValueAt(dob, selectedRow, 5);
                data[5] = dob;
                registeredData.set(selectedRow, data);
                JOptionPane.showMessageDialog(this, "Record Updated Successfully", "Update Record", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Text Fields are Empty!", "Invalid", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void readBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_readBtnActionPerformed
        DefaultTableModel table = (DefaultTableModel) this.employeeTable.getModel();
        table.setRowCount(0);
        for (Object object : registeredData) {
            Vector<Object> data = new Vector<>();
            String[] dataArray = (String[]) object;
            for (String element : dataArray) {
                data.add(element);
            }
            table.addRow(data);
        }
    }//GEN-LAST:event_readBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        DefaultTableModel table = (DefaultTableModel) this.employeeTable.getModel();
        int[] selectedRows = this.employeeTable.getSelectedRows();

        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "No Selected Row to Delete!", "Error", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int del = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this selected row?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (del == JOptionPane.YES_OPTION) {
                for (int i = selectedRows.length - 1; i >= 0; i--) {
                    int modelRow = this.employeeTable.convertRowIndexToModel(selectedRows[i]);
                    table.removeRow(modelRow);
                    registeredData.remove(modelRow);
                }
                JOptionPane.showMessageDialog(this, "Record(s) Deleted", "Delete Record", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

