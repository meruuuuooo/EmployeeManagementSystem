    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        JTextField[] textFields = {this.IdTextField, this.fullnameTextField, this.departmentField, this.jobtitleField, this.salaryTextField};
        DefaultTableModel table = (DefaultTableModel) this.employeeTable.getModel();
        boolean found = false;
        if (!this.searchTextField.getText().isEmpty()) {
            int searchId = Integer.parseInt(this.searchTextField.getText());
            int rowCount = table.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                String[] data = (String[]) registeredData.get(i);
                int employeeId = Integer.parseInt(data[0]);
                if (employeeId == searchId) {
                    this.employeeTable.clearSelection();
                    this.employeeTable.setRowSelectionInterval(i, i);
                    for (int j = 0; j < textFields.length; j++) {
                        textFields[j].setText(table.getValueAt(i, j).toString());
                    }
                    found = true;
                    break;
                }
            }
            if (!found) {
                JOptionPane.showMessageDialog(this, "No employee found with ID: " + searchId, "Not Found", JOptionPane.INFORMATION_MESSAGE);
                readBtnActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(this, "Search Complete", "Search Records", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Search Field is Empty!", "Invalid", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchBtnActionPerformed

