    private boolean isEmpty(JTextField[] textFields) {
        for (JTextField field : textFields) {
            if (field.getText().isEmpty()) {
                return true;
            }
        }
        return false;
    }

    public String dateOfBirth(JComboBox[] comboboxes) {
        StringBuilder dateOfBirth = new StringBuilder();
        for (JComboBox jComboBox : comboboxes) {
            dateOfBirth.append(jComboBox.getSelectedItem().toString() + "-");
        }
        dateOfBirth.deleteCharAt(dateOfBirth.length() - 1);
        return dateOfBirth.toString();
    }

